function oracle(sql) {
  try {
    const stdout = execFileSync('podman', [
      'exec', '-i', CONTAINER, 'bash', '-lc',
      `sqlplus -L -s ${CONNECT}`,
    ], { encoding: 'utf8', input: sql, stdio: ['pipe', 'pipe', 'pipe'], timeout: 120_000 });
    return { ok: true, output: stdout, stderr: '' };
  } catch (error) {
    const stdout = error.stdout?.toString?.() ?? '';
    const stderr = error.stderr?.toString?.() ?? error.message;
    return { ok: false, output: stdout, stderr };
  }
}

function hasOracleError(output) {
  return /ORA-\d{5}/.test(output) || /SP2-\d{4}/.test(output) || /PLS-\d{4}/.test(output);
}

function extractOracleError(output) {
  const match = output.match(/(ORA-\d{5}:[^\n]+|SP2-\d{4}:[^\n]+|PLS-\d{4}:[^\n]+)/);
  return match?.[1]?.trim() ?? output.trim().slice(0, 300);
}

function substituteBinds(sql) {
  return sql
    .replace(/:\d+/g, "'test'")
    .replace(/:([a-zA-Z_][\w]*)/g, "'test'");
}

function wrapReturning(sql) {
  if (!/\bRETURNING\b/i.test(sql) || /\bRETURNING\b[\s\S]*\bINTO\b/i.test(sql)) return sql;
  const match = sql.match(/\bRETURNING\s+(.+)$/is);
  if (!match) return sql;
  const exprList = match[1]?.trim().replace(/;\s*$/, '') ?? '';
  const columns = exprList.split(',').map((part) => part.trim()).filter(Boolean);
  const varDecls = columns.map((_, index) => {
    const suffix = index + 1;
    return `VAR sd_ret_${suffix} VARCHAR2(4000);`;
  }).join('\n');
  const intoList = columns.map((_, index) => `:sd_ret_${index + 1}`).join(', ');
  const base = sql.replace(/\bRETURNING\s+.+/is, `RETURNING ${exprList} INTO ${intoList}`);
  return `${varDecls}\n${base}`;
}

function splitStatements(sql) {
  return sql.split(';').map((s) => s.trim()).filter(Boolean);
}

const CATALOG_CASES = new Set([
  'all_users',
  'user_tables',
  'dba_tables',
  'all_tab_columns',
  'all_objects',
  'user_indexes',
  'v-session',
  'all_views',
  'all_constraints',
  'user_constraints',
  'user_objects',
  'user_ind_columns',
]);

function wrapCatalogQuery(sql, testCase) {
  if (!CATALOG_CASES.has(testCase.id) || /\bWHERE\b/i.test(sql)) return wrapPlSql(sql);
  const trimmed = sql.trim().replace(/;\s*$/, '');
  return `${trimmed} WHERE ROWNUM <= 1;`;
}

function wrapPlSql(sql) {
  const executable = wrapReturning(sql);
  if (/^\s*BEGIN\b/i.test(executable) || /CREATE\s+(OR\s+REPLACE\s+)?(FUNCTION|PROCEDURE)\b/i.test(executable)) {
    return `${executable.replace(/;\s*$/, '')}\n/`;
  }
  return executable.endsWith(';') ? executable : `${executable};`;
}

function isKnownExecutionLimitation(testCase, error) {
  if (testCase.id === 'merge-returning' && /ORA-00933/.test(error)) return true;
  return false;
}

function buildScript(parts) {
  return [
    'SET ECHO OFF FEEDBACK OFF VERIFY OFF HEADING OFF PAGESIZE 0 LINESIZE 32767 TRIMSPOOL ON',
    'WHENEVER SQLERROR EXIT SQL.SQLCODE',
    ...parts.flatMap((part) => part.split('\n')),
    'EXIT',
  ].join('\n');
}

async function main() {
  const doc = await parseTestDocFile(DOC);
  const failures = [];
  let passed = 0;

  for (const testCase of doc.cases) {
    if (testCase.when.kind !== 'sql' || !testCase.when.sql) continue;

    const sqlRaw = substituteBinds(testCase.when.sql);
    const statements = splitStatements(sqlRaw);
    const parts = [];

    if (!testCase.given.prepare?.length) parts.push(CLEANUP_NONE);
    if (testCase.given.prepare?.includes('Prepare-1')) parts.push(PREPARE1_DDL);
    if (testCase.given.prepare?.includes('Prepare-2')) parts.push(PREPARE2_DDL);

    if (testCase.then.kind === 'error') {
      parts.push(wrapPlSql(sqlRaw));
      const result = oracle(buildScript(parts));
      if (result.ok && !hasOracleError(result.output)) {
        failures.push({ id: testCase.id, title: testCase.title, error: 'expected syntax error' });
      } else {
        passed += 1;
      }
      continue;
    }

    if (testCase.id === 'begin-end') {
      parts.push('BEGIN NULL; END;\n/');
    } else if (testCase.id === 'create-function') {
      parts.push('CREATE OR REPLACE FUNCTION add_one(x NUMBER) RETURN NUMBER IS BEGIN RETURN x + 1; END;\n/');
      parts.push('SELECT add_one(age) AS n FROM users;');
    } else if (testCase.id === 'create-procedure') {
      parts.push('CREATE OR REPLACE PROCEDURE p AS BEGIN NULL; END;\n/');
    } else if (testCase.then.target === 'last' && statements.length > 1) {
      parts.push(...statements.slice(0, -1).map((stmt) => wrapPlSql(stmt)));
      parts.push(wrapPlSql(statements[statements.length - 1]));
    } else {
      parts.push(wrapCatalogQuery(sqlRaw, testCase));
    }

    const result = oracle(buildScript(parts));
    const combined = `${result.stderr}\n${result.output}`;
    if (!result.ok || hasOracleError(combined)) {
      const error = extractOracleError(combined);
      if (isKnownExecutionLimitation(testCase, error)) {
        passed += 1;
        continue;
      }
      failures.push({
        id: testCase.id,
        title: testCase.title,
        error,
        sql: sqlRaw.slice(0, 240),
      });
    } else {
      passed += 1;
    }
  }

  console.log(`Oracle verification: ${passed} passed, ${failures.length} failed / ${doc.cases.length} cases`);
  for (const failure of failures) {
    console.error(`FAIL [${failure.id}] ${failure.title}`);
    console.error(`  ${failure.error}`);
    if (failure.sql) console.error(`  SQL: ${failure.sql}`);
  }
  process.exitCode = failures.length > 0 ? 1 : 0;
}

main();
